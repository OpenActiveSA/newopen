// Create the feather-icons directory
const featherDir = path.join(__dirname, '..', 'src', 'assets', 'feather-icons');
if (!fs.existsSync(featherDir)) {
  fs.mkdirSync(featherDir, { recursive: true });
}

// Download each icon
featherIcons.forEach(iconName => {
  const svgContent = iconSVGs[iconName];
  if (svgContent) {
    const fileName = `${iconName}.svg`;
    const filePath = path.join(featherDir, fileName);
    fs.writeFileSync(filePath, svgContent);
    console.log(`Downloaded: ${fileName}`);
  }
});

